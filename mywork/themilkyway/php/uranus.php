<h1 class="header">Uranus</h1>
<p>
    <span class="bold">Uranus</span> was the first ever discovered planet by the aid of a 
    telescope all the way back in 1781 by astronomer William Herschel, even though he didn't 
    think at the time it was a planet.
</p>
<p class="sub-header">Things to know: </p>
<ul>
    <li class="body-14">&bull;&nbsp; Uranus is pretty big in comparison to Earth, whom Uranus is roughly
        4 times larger than. 
    </li>
    <li class="body-14">&bull;&nbsp; Uranus takes about 17 hours to rotate fully and 84 Earth years to orbit 
        the sun.
    </li>
    <li class="body-14">&bull;&nbsp; Even though they aren't as vibrant or massive as Saturn's, Uranus also 
        has rings. The 13 rings surrounding Uranus are darker in the inner and get brighter the further out.
    </li>
</ul>
