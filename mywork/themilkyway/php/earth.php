<h1 class="header">Earth</h1>
<p>
    Welcome home, <span class="bold">Earth</span> is the planet we all know and love. Earth is home
    to thousands of living creatures and is currently the only known place in the universe
    that has living creatures.
</p>
<p>
    Earth is also the only planet in our solar system which contains liquid water.
    Scientists believe liquid water is the key to inhabiting living creatures.
    In size, Earth is the fifth smallest planet in our solar system, and it's only 
    slightly larger than Venus.
</p>
<p class="sub-header">Things to know: </p>
<ul>
    <li class="body-14">&bull;&nbsp; Just for reference, if the sun were as big as a front door, then Earth
        would only be the size of a nickel.
    </li>
    <li class="body-14">&bull;&nbsp; The atmosphere surrounding Earth is strong enough to protect us from most
        incomming meteoroids and rocks, which break up upon entering the atmosphere.
    </li>
    <li class="body-14">&bull;&nbsp; The atmosphere surrounding Earth is made up of 78% nitrogen, 21% oxygen and 1% misc.
        This is known as the perfect balance to breathe and live.
    </li>
</ul>
