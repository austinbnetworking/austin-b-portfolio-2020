<h1 class="header">Mercury</h1>
<p>
    The smallest and closest planet to the sun, <span class="bold">Mercury</span> is a rough rocky 
    planet. Because of it's proximity to the sun, if we stood on Mercury the sun would appear to be 3 
    times larger and more than 7 times brighter.
</p>
<p>
    Mercury has a cratered surface that could be compared to our Moon. The atmosphere is rough and tempuratures
    much more extreme, meaning Mercury will not be capable of maintaining life as we know it. Even though it is the 
    closest planet to the Sun, it's actually not the hottest. 
</p>
<p class="sub-header">Things to know: </p>
<ul>
    <li class="body-14">&bull;&nbsp; Mercury is tiny, only just slightly bigger than our Moon.
    </li>
    <li class="body-14">&bull;&nbsp; It takes roughly 176 Earth days for one full day-night cycle on
        Mercury but only takes 88 Earth days to orbit the Sun. 
    </li>
    <li class="body-14">&bull;&nbsp; Daytime tempuratures can reach 430 degrees Celsius and drop to 
        -180 degrees Celsius at night. 
    </li>
</ul>
