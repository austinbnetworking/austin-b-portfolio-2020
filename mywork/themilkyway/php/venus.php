<h1 class="header">Venus</h1>
<p>
    "Earths twin", <span class="bold">Venus</span> is roughly the same size and structure of Earth. 
    They are obviously radically different in their nature. Venus is extremely toxic and 
    dangerous because it traps heat inside its very thick atmosphere.
</p>
<p>
    At the surface of Venus, the air pressure would be 90 times that of Earths. That would crush any 
    life as we know it, similar to a mile below the ocean. Because of the mentioned thick atmosphere, 
    Venus is also the hottest planet in our solar system.
</p>
<p class="sub-header">Things to know: </p>
<ul>
    <li class="body-14">&bull;&nbsp; Venus spins backwards, causing one day to last an entire year in 
        Earth days.
    </li>
    <li class="body-14">&bull;&nbsp; The first planet explored by spacecraft in our solar system, with 
        more than 40 spacecraft explorations as of today.
    </li>
</ul>
