<h1 class="header">Mars</h1>
<p>
    <span class="bold">Mars</span> is an impressive planet and quite an interesting one.
        Mars is a cold dusty planet that comes in smaller than our own Earth. Scientists have been
        attempting to explore Mars for a long time, with many explorations in the past and many more planned 
        for the future.
</p>
<p>
    Recently in February of 2021, NASA landed a Rover on Mars. This rover named "Perserverance" is 
    only the second of it's kind, both roaming Mars right now. Mars has a very similar day cycle 
    of over 24 hours, but a year cycle of 687 Earth days.
</p>
<p class="sub-header">Things to know: </p>
<ul>
    <li class="body-14">&bull;&nbsp; Just for reference, if the Earth was the size of a dime, Mars would 
        be comparable to the size of an aspirin tablet. 
    </li>
    <li class="body-14">&bull;&nbsp; Mars' atmosphere is rough and thin, meaning at the moment any life
        there will only be capable with spacesuits. 
    </li>
    <li class="body-14">&bull;&nbsp; Mars is known as the red planet because it is actually very rusty
        and that causes the soil and atmosphere have a red appearance.
    </li>
</ul>
