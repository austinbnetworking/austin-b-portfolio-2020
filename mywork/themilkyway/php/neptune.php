<h1 class="header">Neptune</h1>
<p>
    <span class="bold">Neptune</span> is incredibly dark, cold and home to supersonic winds. It is 
    known as an ice giant. 
</p>
<p>
    Neptune is the farthest planet from the sun in our solar system, which is why it is so cold and 
    dark. Neptune is so dark that it is actually not visible to the naked eye. That might be suprising, 
    considering that Neptune is estimated to be 4 times wider than Earth.
</p>
<p class="sub-header">Things to know: </p>
<ul>
    <li class="body-14">&bull;&nbsp; Neptune takes about 16 hours to rotate once, and 165 Earth years to rotate 
        the sun.
    </li>
    <li class="body-14">&bull;&nbsp; Neptune was actually predicted mathmematically before it was ever discovered.
    </li>
    <li class="body-14">&bull;&nbsp; There has only been one spacecraft to ever visit Neptune up close, 
        the Voyager 2 by NASA seen Neptune as it flew out of the solar system. 
    </li>
</ul>
