<h1 class="header">Saturn</h1>
<p>
    The planet with massive rings surrounding it, <span class="bold">Saturn</span> is the second largest
    planet in our solar system. Much like the other Giant planet <span onclick="change_page('jupiter')" class="bold" style="cursor: pointer;">Jupiter</span>
    , Saturn is a giant ball of gas.
</p>
<p>
    The rings that surround Saturn are huge and magnificent, however, they are not the only ones that exist in our solar system. Those rings, like 
    some others, are made up of chunks of ice and rock. Saturn is the sixth farthest planet from the sun.
</p>
<p class="sub-header">Things to know: </p>
<ul>
    <li class="body-14">&bull;&nbsp; Earth could span across saturn 9 times, and that doesn't include the rings 
        surrounding Saturn.
    </li>
    <li class="body-14">&bull;&nbsp; Even though no one knows precisely how long a day cycle is, scientists estimate 
        it takes around 10.7 hours to rotate once, and 29 Earth years to rotate the sun.
    </li>
    <li class="body-14">&bull;&nbsp; Another planet with tons of moons, Saturn has 53 confirmed moons and 
        29 moons awaiting confirmation. Some could even harvest life.
    </li>
</ul>
