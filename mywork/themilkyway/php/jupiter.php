<h1 class="header">Jupiter</h1>
<p>
    <span class="bold">Jupiter</span> is a massive planet that is by far the 
    largest planet in our solar system. The size of Jupiter is twice as large 
    as all other planets combined!
</p>
<p>
    Jupiter is the fifth farthest planet from the sun and is labeled a gas giant. 
    That means Jupiter does not contain anywhere near the ingredients to maintain life.
    That hasn't stopped scientists from using it for study. NASA even landed a spacecraft 
    on Jupiter in 2016, named <a href="https://solarsystem.nasa.gov/missions/juno/overview/" target="_blank">Juno orbiter. </a> 
</p>
<p class="sub-header">Things to know: </p>
<ul>
    <li class="body-14">&bull;&nbsp; Just for reference, if Earth was the size of a 
        grape, Jupiter would be the relevant size of a basketball. Scientists say 
        that 11 earths could fit inside Jupiter.
    </li>
    <li class="body-14">&bull;&nbsp; Jupiter lacks an Earth like surface, it's gaseous and 
        is made up of mostly hydrogen and helium.
    </li>
    <li class="body-14">&bull;&nbsp; Jupiter has 75 moons! Some of them are even believed to
        have ocean floors that could be cabable of maintaining life.
    </li>
    <li class="body-14">&bull;&nbsp; Has a short day cycle but longer year cycle. Jupiter fully 
        rotates once every 10 hours, but takes 12 Earth years to orbit the sun.
    </li>
    <li class="body-14">&bull;&nbsp; Jupiter is home to a massive storm, bigger than Earth that 
        has been raging on for hundreds of years.
    </li>
</ul>
